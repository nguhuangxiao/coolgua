package com.coolgua.signup.domain;

import java.util.Date;

public class Tickets {

	private String id;
	private String openId;
	private String ticketsNum;
	private String ticketNumUrl;
	private Date createTime;
	private String eventId;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getOpenId() {
		return openId;
	}

	public void setOpenId(String openId) {
		this.openId = openId;
	}

	public String getTicketsNum() {
		return ticketsNum;
	}

	public void setTicketsNum(String ticketsNum) {
		this.ticketsNum = ticketsNum;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

	public String getTicketNumUrl() {
		return ticketNumUrl;
	}

	public void setTicketNumUrl(String ticketNumUrl) {
		this.ticketNumUrl = ticketNumUrl;
	}

	public String getEventId() {
		return eventId;
	}

	public void setEventId(String eventId) {
		this.eventId = eventId;
	}
	
}
